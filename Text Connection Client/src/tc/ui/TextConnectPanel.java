    private void textAreaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textAreaKeyPressed
        if (evt.getKeyChar() == '\n') {
			String selected = this.textArea.getSelectedText();
			System.out.println("Selection: "+selected);
			this.startIndex = this.text.getText().indexOf(selected);
			this.endIndex = this.startIndex + selected.length();
			
			Selection selection = new Selection(text, startIndex, endIndex);
			if (selection1 == null) {
				selection1 = selection;
				this.part1.setText(selection.toString());
			} else {
				selection2 = selection;
				this.part2.setText(selection.toString());
			}
		}
    }//GEN-LAST:event_textAreaKeyPressed

