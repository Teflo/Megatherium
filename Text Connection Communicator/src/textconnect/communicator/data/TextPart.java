public class TextPart {
	private int id;
	private int textID;
	private int line;
	private int column;
	private String item;

	public int getID() {
		return id;
	}

	public int getTextID() {
		return textID;
	}

	public int getLine() {
		return line;
	}

	public int getColumn() {
		return column;
	}

	public String getItem() {
		return item;
	}
	
	
}
